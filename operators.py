num1=int(input("enter first number;"))
operator=input("enter operator;")
num2=int(input("enter second number;"))
# if operator is (+|-|/|*|%|)then print out number 1(+|-|*|/|%)number 2
if operator =="+":
    print(num1+num2)
elif operator=="-":
    print(num1-num2) 
elif operator=="%":
    print(num1%num2)
elif operator=="*":
    print(num1*num2)
elif operator=="/":
    print(num1/num2)
else:
    print("NOT VALID OPERATOR")

    
        
                    
                
            
        